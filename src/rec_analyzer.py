class LectureAnalyzer:
    """Lecture analysis service.

    Initialize once at service startup, then call process() for each recording.
    Models are loaded once during initialization for efficiency.

    Example:
        analyzer = LectureAnalyzer(pyannote_api_key="...")
        result = analyzer.process("lecture.mp3", "record_123", "group_A")
    """

    def __init__(
        self,
    ):
        """Initialize the analyzer service.

        Loads all ML models once. This should be called once at service startup.

        Args:
            pyannote_api_key: API key for pyannote speaker diarization.
            llm_base_url: Base URL for the LLM API endpoint.
        """
        self.device = "cuda:0" if torch.cuda.is_available() else "cpu"
        self.torch_dtype = torch.float16 if torch.cuda.is_available() else torch.float32

        self.diarization_pipeline = self._load_diarization_pipeline(
            os.getenv("HUGGINGFACEHUB_API_TOKEN")
        )
        self.speech_pipe, self.processor = self._load_whisper()
        self.f5tts, self.accentizer = self._load_tts()

        # LLM with reasoning for all tasks
        self.llm = ChatOpenAI(
            model=os.getenv("LLM_NAME"),
            base_url=os.getenv("LLM_URL"),
            api_key=os.getenv("LLM_API_KEY"),
            reasoning={"effort": "high", "summary": None},
            temperature=0,
        )

        # JSON parsers for structured outputs (works with reasoning mode)
        self.questions_parser = JsonOutputParser(pydantic_object=QuestionsResult)
        self.podcast_parser = JsonOutputParser(pydantic_object=PodcastScript)

        # RAG по умолчанию
        self.rag = LectureRAG()

    def _load_diarization_pipeline(self, api_key: str) -> Pipeline:
        """Load pyannote speaker diarization pipeline."""
        return Pipeline.from_pretrained(
            "pyannote/speaker-diarization-3.1",
            use_auth_token=api_key,
        ).to(torch.device(self.device))

    def _load_whisper(self):
        """Load Whisper speech recognition model."""
        model_id = "openai/whisper-large-v3"
        model = AutoModelForSpeechSeq2Seq.from_pretrained(
            model_id,
            dtype=self.torch_dtype,
            low_cpu_mem_usage=True,
            use_safetensors=True,
        )
        model.to(self.device)

        processor = AutoProcessor.from_pretrained(model_id)
        pipe = pipeline(
            "automatic-speech-recognition",
            model=model,
            tokenizer=processor.tokenizer,
            feature_extractor=processor.feature_extractor,
            dtype=self.torch_dtype,
            device=self.device,
        )
        return pipe, processor

    def _load_tts(self):
        """Load F5TTS and RUAccent for podcast generation."""
        f5tts = F5TTS(
            ckpt_file="F5TTS/ckpts/model_v4.safetensors",
            vocab_file="F5TTS/ckpts/vocab.txt",
            device=self.device,
        )
        accentizer = RUAccent()
        accentizer.load(
            omograph_model_size="turbo3.1", use_dictionary=True, tiny_mode=False
        )
        return f5tts, accentizer

    # =========================================================================
    # LLM Methods (langchain + structured outputs)
    # =========================================================================

    def _extract_llm_response(self, response) -> str:
        """Extract text from LLM response (handles reasoning mode).

        Args:
            response: AIMessage from llm.invoke().

        Returns:
            Extracted text content.
        """
        content = response.content
        if isinstance(content, list):
            for item in reversed(content):
                if isinstance(item, dict) and "text" in item:
                    return item["text"]
            return "".join(
                item.get("text", "") if isinstance(item, dict) else str(item)
                for item in content
            )
        return content

    def _generate_abstract(self, lecture_text: str) -> str:
        """Generate lecture abstract (markdown).

        Args:
            lecture_text: Full lecture transcription.

        Returns:
            Markdown abstract text.
        """
        response = self.llm.invoke(
            f"{ABSTRACT_PROMPT}\n\nТекст лекции:\n{lecture_text}"
        )
        return latex_to_md(self._extract_llm_response(response))

    def _generate_questions(self, lecture_text: str) -> List[str]:
        """Generate self-check questions using structured output with reasoning.

        Args:
            lecture_text: Full lecture transcription.

        Returns:
            List of 10-12 questions.
        """
        questions_prompt = PromptTemplate(
            template="""
Ты — ИИ-методист. Получишь текст лекции и должен составить список вопросов.

Сформируй строго структурированный JSON, соответствующий схеме ниже.

{format_instructions}

Инструкция:
1. Сформулируй ровно 12 вопросов.
2. Следуй порядку изложения лекции.
3. Используй только русский язык.
4. Никаких комментариев, пояснений, разметки.
5. Для оформления формул используй markdown форматирование

Верни строго корректный JSON.
Текст лекции:
{lecture_text}
""",
            input_variables=["lecture_text"],
            partial_variables={
                "format_instructions": self.questions_parser.get_format_instructions()
            },
        )
        try:
            result = self.questions_parser.invoke(
                self.llm.invoke(questions_prompt.format(lecture_text=lecture_text))
            )
            # Extract question strings from Question objects
            questions = result.get("questions", [])
            return [
                q.get("question", q) if isinstance(q, dict) else str(q)
                for q in questions
            ]
        except Exception:
            return []

    def _generate_mindmap(self, lecture_text: str, max_retries: int = 10) -> dict:
        """Generate mind map structure.

        Args:
            lecture_text: Full lecture transcription.
            max_retries: Maximum number of retry attempts.

        Returns:
            Dict with mind map structure.
        """
        for attempt in range(max_retries):
            try:
                response = self.llm.invoke(
                    f"{MINDMAP_PROMPT}\n\nТекст лекции:\n{lecture_text}"
                )
                return parse_json(self._extract_llm_response(response))
            except Exception as e:
                logger.warning(
                    "Mindmap: попытка %d/%d не удалась: %s", attempt + 1, max_retries, e
                )
        logger.error("Не удалось сгенерировать mindmap после %d попыток", max_retries)
        return {"title": "Не удалось сгенерировать", "nodes": []}

    def _analyze_emotion(self, text: str) -> str:
        """Analyze emotional tone of a text fragment.

        Args:
            text: Text fragment to analyze.

        Returns:
            Adjectives describing the fragment.
        """
        response = self.llm.invoke(f"{EMOT_AN_PROMPT}\n\nФрагмент:\n{text}")
        return self._extract_llm_response(response).strip()

    def _generate_podcast_script(
        self, lecture_text: str, questions: List[str], max_retries: int = 10
    ) -> dict:
        """Generate podcast script using structured output with reasoning.

        Args:
            lecture_text: Full lecture transcription.
            questions: List of questions for the podcast.
            max_retries: Maximum number of retry attempts.

        Returns:
            Dict with podcast parts.
        """
        questions_str = "\n".join(f"{i + 1}. {q}" for i, q in enumerate(questions))
        prompt = PromptTemplate(
            template="""{base_prompt}

            {format_instructions}

            Вопросы:
            {questions_str}

            Лекция:
            {lecture_text}""",
            input_variables=["lecture_text", "questions_str"],
            partial_variables={
                "base_prompt": PODCAST_PROMPT,
                "format_instructions": self.podcast_parser.get_format_instructions(),
            },
        )
        for attempt in range(max_retries):
            try:
                response = self.llm.invoke(
                    prompt.invoke(
                        {"lecture_text": lecture_text, "questions_str": questions_str}
                    )
                )
                text = self._extract_llm_response(response)
                return self.podcast_parser.invoke(text)
            except Exception as e:
                logger.warning(
                    "Подкаст: попытка %d/%d не удалась: %s", attempt + 1, max_retries, e
                )
        logger.error("Не удалось сгенерировать подкаст после %d попыток", max_retries)
        return {"parts": []}

    def _clean_podcast_part(self, part: dict) -> dict:
        """Clean a single podcast part for TTS.

        Args:
            part: Dict with presenter and lector text.

        Returns:
            Cleaned podcast part as dict.
        """
        presenter_text = part.get("presenter", "")
        lector_text = part.get("lector", "")

        presenter_response = self.llm.invoke(
            f"{CLEAN_PODCAST_PROMPT}\n\nТекст:\n{presenter_text}"
        )
        cleaned_presenter = self._extract_llm_response(presenter_response)

        lector_response = self.llm.invoke(
            f"{CLEAN_PODCAST_PROMPT}\n\nТекст:\n{lector_text}"
        )
        cleaned_lector = self._extract_llm_response(lector_response)

        return {"presenter": cleaned_presenter, "lector": cleaned_lector}

    def _clean_podcast(self, podcast_script: dict) -> dict:
        """Clean entire podcast script for TTS.

        Args:
            podcast_script: Dict with podcast parts.

        Returns:
            Cleaned podcast script formatted for generate_podcast function.
        """
        cleaned_parts = {}

        intro = random.choice(PODCAST_INTROS)
        cleaned_parts["part_0"] = intro

        for i, part in enumerate(podcast_script.get("parts", [])):
            cleaned_parts[f"part_{i + 1}"] = self._clean_podcast_part(part)

        outro = random.choice(PODCAST_OUTROS)
        cleaned_parts[f"part_{len(podcast_script.get('parts', [])) + 1}"] = outro

        return cleaned_parts

    def process(
        self,
        recording_path: str,
        record_id: str,
        groups: List[str],
        lection_date: datetime.date = None,
        use_rag: bool = True,
    ) -> dict:
        """Process a single lecture recording.

        Args:
            recording_path: Path to audio file (mp3 or wav).
            record_id: Unique identifier for this recording.
            groups: List of student group identifiers.
            lection_date: Date of the lecture (defaults to today).

        Returns:
            Dict with all analysis results.

        Raises:
            FileNotFoundError: If recording_path does not exist.
        """
        if lection_date is None:
            lection_date = datetime.date.today()

        audio = prepare_audio(recording_path)

        diarization_result = diarize(audio.wav_path, self.diarization_pipeline)

        transcription = transcribe(
            audio, diarization_result, self.speech_pipe, self.processor
        )

        text_analysis = analyze_text(transcription.chunks)

        llm_result = self._llm_analyze(transcription.lecture_text)

        clean_fragment_path = extract_clean_fragment(transcription.chunks, audio)
        if llm_result["podcast_text_ok"]:
            podcast_path = generate_podcast(
                llm_result["podcast_text"],
                clean_fragment_path,
                audio.wav_path,
                self.f5tts,
                self.accentizer,
            )
        else:
            podcast_path = None

        clear_gpu_cache()

        if use_rag:
            try:
                self.rag.add_lecture(
                    lecture_text=transcription.lecture_text,
                    student_groups=groups,
                    lecture_date=lection_date,
                    record_id=record_id,
                )
            except Exception as e:
                logger.error("Не удалось добавить лекцию в RAG (record_id=%s): %s", record_id, e)

        return {
            "lecture_text": transcription.lecture_text,
            "abstract_text": llm_result["abstract_text"],
            "speech_speed": text_analysis.speed,
            "mindmap": llm_result["mind_map"],
            "popular_words_no_stopw": text_analysis.popular_words_no_stopw,
            "popular_words_w_stopw": text_analysis.popular_words_w_stopw,
            "conversation_static": diarization_result.diagram,
            "lecture_timeline": text_analysis.transcripted_chunks,
            "questions": llm_result["questions"],
            "podcast": podcast_path,
        }

    def get_results(
        self,
        recording_path: str,
        record_id: str,
        group: str,
        lection_date: datetime.date = None,
    ) -> str:
        """Process recording and return results as JSON string.

        This method provides backward compatibility with the old API.

        Args:
            recording_path: Path to audio file.
            record_id: Unique identifier for this recording.
            group: Student group identifier.
            lection_date: Date of the lecture.

        Returns:
            JSON string with all analysis results.
        """
        result = self.process(recording_path, record_id, [group], lection_date)
        return json.dumps(result, default=str)

    def _llm_analyze(self, lecture_text: str) -> dict:
        """Analyze lecture using LLM with structured outputs.

        Generates abstract, questions, mind map, podcast text.

        Args:
            lecture_text: Full lecture transcription.

        Returns:
            Dict with abstract_text, questions, mind_map, podcast_text.
        """
        abstract_text = self._generate_abstract(lecture_text)

        mind_map = self._generate_mindmap(lecture_text)

        questions = self._generate_questions(lecture_text)

        podcast_script = self._generate_podcast_script(lecture_text, questions)
        podcast_text_ok = len(podcast_script.get("parts", [])) > 0

        podcast_text = self._clean_podcast(podcast_script) if podcast_text_ok else {}

        return {
            "abstract_text": abstract_text,
            "mind_map": mind_map,
            "questions": questions,
            "podcast_text": podcast_text,
            "podcast_text_ok": podcast_text_ok,
        }
