def clear_gpu_cache() -> None:
    """Clear the GPU cache if CUDA is available."""
    if torch.cuda.is_available():
        torch.cuda.empty_cache()


def latex_to_md(text: str) -> str:
    """Convert LaTeX syntax to Markdown math syntax.

    Args:
        text: Text to convert.

    Returns:
        Converted text with Markdown math delimiters.
    """
    text = re.sub(r"\\\(\s*(.*?)\s*\\\)", r"$\1$", text)
    text = re.sub(r"\\\[\s*(.*?)\s*\\\]", r"$$\1$$", text, flags=re.DOTALL)
    return text


def parse_json(text: str) -> dict:
    """Extract and clean a JSON string generated by an LLM.

    Includes multiple fix steps: unicode normalization, typographic quotes,
    bare keys/values, trailing commas.

    Args:
        text: Raw text containing a JSON object.

    Returns:
        Parsed dict.

    Raises:
        ValueError: If JSON cannot be parsed after all fix attempts.
    """
    text = text.strip()
    start = text.find("{")
    end = text.rfind("}")
    if start == -1 or end == -1:
        raise ValueError("В тексте не найден JSON-объект (нет {}).")
    text = text[start : end + 1]

    # Remove invisible characters
    text = re.sub(r"[\u0000-\u001F\u007F-\u009F\u00AD\u202f\xa0]", " ", text)

    # Normalize typographic quotes
    text = (
        text.replace("\u201c", '"')
        .replace("\u201d", '"')
        .replace("\u2032", '"')
        .replace("\u2033", '"')
    )

    # Fix bare backslashes
    text = re.sub(r'\\(?!["\\/bfnrtu])', r"\\\\", text)

    # Attempt 1: direct parse
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    # Attempt 2: fix bare keys/values and single quotes
    def fix_key_value_and_quotes(match):
        key = match.group(1).strip()
        val = match.group(2).strip()
        val = re.sub(r",$", "", val)

        key_fixed = key.replace('"', '\\"')
        if not (key_fixed.startswith('"') and key_fixed.endswith('"')):
            key_fixed = f'"{key_fixed}"'

        if not (val.startswith('"') and val.endswith('"')):
            val = val.replace("'", '"')
            val = val.replace('"', '\\"')
            val = f'"{val}"'
        elif val.startswith("'") and val.endswith("'"):
            val = val[1:-1].replace('"', '\\"').replace("'", '"')
            val = f'"{val}"'

        return f"{key_fixed}: {val}"

    text_fixed = re.sub(
        r'(\w+|"[^"]+"|' + r"'[^']+'" + r")\s*:\s*([^,{}\n]+)",
        fix_key_value_and_quotes,
        text,
        flags=re.DOTALL,
    )

    # Remove trailing commas
    text_fixed = re.sub(r",\s*([}\]])", r"\1", text_fixed)

    try:
        return json.loads(text_fixed)
    except json.JSONDecodeError as e:
        raise ValueError(
            f"Не удалось распарсить JSON после исправлений. "
            f"Ошибка: {e}\nТекст: {text_fixed[:500]}..."
        )


def fill_silence_intervals(
    data: List[Tuple[str, float, float]],
) -> List[List]:
    """Fill intervals when no words were spoken.

    Args:
        data: List of [speaker_id, start, end] tuples.

    Returns:
        Data with silence intervals (speaker_id=3) filled in.
    """
    filled_data = []

    if data[0][1] > 0:
        filled_data.append([3, 0, data[0][1]])
    for i, entry in enumerate(data):
        speaker, start, end = entry
        filled_data.append(entry)

        if i < len(data) - 1:
            next_start = data[i + 1][1]
            if end < next_start:
                filled_data.append([3, end, next_start])
    return filled_data


